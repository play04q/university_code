Vm=300;
Vt=240;
l=5000;
h=1500;
beit=150/180*pi;
f0=100e6;
Ut=10;
fai=pi/18;
omig0=2*pi*f0;
c=3e8;
Vr=sqrt(Vm^2+Vt^2-2*Vm*Vt*cos(beit));
a=asin(Vt/Vr*sin(beit));
t=1e-4:1e-4:10;
t1=(1e-9:1e-9:1e-4)';
T=t';
l1=l-Vm.*t*cos(30/180*pi)-Vt.*t;
l2=h-Vm.*t*sin(30/180*pi);
R=sqrt(l1.^2+l2.^2);
gama=atan(l2./l1);
sit=30/180*pi-a-gama;
VR=Vr.*cos(sit);
ut=Ut.*cos(omig0.*t+fai);%发射信号
lanmt0=c/f0;
Fd=2.*VR/lanmt0;
omigd=2*pi.*Fd;
u=Ut./(R.^2).*cos((omig0+omigd).*t+fai);%回波信号
U=u';
plot(t,R);
title('弹目距离');
huibo=[T,U];